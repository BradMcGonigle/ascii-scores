interface AsciiSportIconProps {
  league: League;
  variant?: "default" | "compact" | "boxed";
  className?: string;
}

/**
 * Renders an ASCII art icon for a sport
 */
export function AsciiSportIcon({
  league,
  variant = "default",
  className = "",
}: AsciiSportIconProps) {
  const icons = {
    default: SPORT_ICONS,
    compact: SPORT_ICONS_COMPACT,
    boxed: SPORT_ICONS_BOXED,
  };

  const icon = icons[variant][league];

  if (variant === "compact") {
    return (
      <span className={`font-mono ${className}`} aria-label={`${league.toUpperCase()} icon`}>
        {icon}
      </span>
    );
  }

  return (
    <pre
      className={`font-mono text-terminal-fg leading-none ${className}`}
      aria-label={`${league.toUpperCase()} icon`}
    >
      {icon.trim()}
    </pre>
  );
}

